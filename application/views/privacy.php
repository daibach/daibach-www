<h2 class="sr-only">My privacy policy</h2>

<h2>Collecting information</h2>
<p>This site only collects information that is absolutely essential to the running of site. This includes anonymous analytics and information you've entered into forms when contacting me or leaving a comment.</p>
<p>Some parts of the site use services from other places such as Twitter or YouTube.  Where I can, I'll let you know in advance that these services will be used so you can avoid them if you want.</p>

