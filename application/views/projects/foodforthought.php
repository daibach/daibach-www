    <p>In the summer of 2011 I took part, as a mentor, in <a href="https://youngrewiredstate.org">Young Rewired State</a>, a week designed to encourage coding in those aged under 18. Based in the Cardiff centre (hosted by <a href="http://www.sequence.co.uk">Sequence</a>), I was one of the mentors for 17 year old <a href="http://joel-murphy.com/young-rewired-state-2011/">Joel Murphy</a>.</p>

    <p>Joel worked on building a web app to display data from the Food Standards Agency's <a href="http://ratings.food.gov.uk">Food Hygiene Rating Scheme</a>. The app helped people access a venue's hygiene rating before buying or eating food using geo-location or traditional search with built in maps.</p>

    <p>Joel wrote the app using HTML 5, JavaScript and PHP with data scraped from the FSA's website.</p>

    <p>Food for Thought won an award for the best use of government data after a demonstration to a panel of judges at the end of the week. It was also featured in <a href="http://www.cio.co.uk/article/3301886/young-gifted-and-british/">CIO magazine</a>.</p>

    <p>Joel now studies Computer Science at Cardiff University and went on to participate in Young Rewired State 2012 and will be a mentor in 2013.</p>

