    <p>The GOV.UK website provides links to services provided by the various bits of local government around the UK. GOV.UK prefers to link directly to the service on the local authorities website rather than the homepage, so that the user gets straight to what they want to do.</p>
    <p>To provide these deep links, the GOV.UK site used a database of around 94,000 URLs collected in a service called Local Directgov. This database was managed by the local authorities and imported into GOV.UK on a daily basis.</p>
    <p>The UK Local Gov Services Explorer was an app to help visualise what data was within the Local Directgov database.</p>
    <p>From the app, you could explore each of the services and see what URLs had been provided by each local authority. Similarly, you could also look at what services a particular local authority was providing.</p>
    <p>To help understand the quality of the URLs, the app regularly reviewed the dataset looking for particular traits such as 404 pages or redirects to the homepage. It also dealt with various idiosyncrasies of several poorly configured local government websites.</p>
    <p>The app was used by the team at the Government Digital Service to track down problems and to help target fixes to particular subsets of the data.</p>
    <p>In August 2016, GOV.UK replaced the Local Directgov database with a new system that also carried out many of the functions of this app. As a result, Local Directgov was closed and this service became redundant.</p>
